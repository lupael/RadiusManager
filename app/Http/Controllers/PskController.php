     * Store a newly created resource in storage.
     *
     * @param \Illuminate\Http\Request $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'psk_network_name' => 'required|string|max:255',
            'psk_password' => 'required|string|max:255',
        ]);
        \Cache::forever('psk_network_name', $request->psk_network_name);
        \Cache::forever('psk_password', $request->psk_password);
        return redirect('psk');
    }

